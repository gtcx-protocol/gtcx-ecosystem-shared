// ============================================================================
// STARTUP AND GRACEFUL SHUTDOWN
// ============================================================================

async function startup() {
  try {
    // Initialize Redis connection
    await redis.connect();
    logger.info('Redis connected successfully');
    
    // Initialize database connection
    await dbService.initialize();
    logger.info('Database connected successfully');
    
    // Start the bot
    await bot.launch();
    logger.info('GCTX Telegram Bot started successfully! üöÄ');
    
    // Set bot commands for menu
    await bot.telegram.setMyCommands([
      { command: 'start', description: 'üöÄ Begin GCTX onboarding journey' },
      { command: 'dashboard', description: 'üìä Access your personalized dashboard' },
      { command: 'verify', description: '‚úÖ Complete verification process' },
      { command: 'demo', description: 'üé™ Experience interactive demo' },
      { command: 'status', description: 'üìà Check your account status' },
      { command: 'help', description: '‚ùì Get help and support' }
    ]);
    
    logger.info('Bot commands menu set successfully');
    
  } catch (error) {
    logger.error('Failed to start bot:', error);
    process.exit(1);
  }
}

// Graceful shutdown
process.once('SIGINT', async () => {
  logger.info('Received SIGINT, shutting down gracefully...');
  
  try {
    await bot.stop('SIGINT');
    await redis.disconnect();
    await dbService.close();
    logger.info('Graceful shutdown completed');
    process.exit(0);
  } catch (error) {
    logger.error('Error during shutdown:', error);
    process.exit(1);
  }
});

process.once('SIGTERM', async () => {
  logger.info('Received SIGTERM, shutting down gracefully...');
  
  try {
    await bot.stop('SIGTERM');
    await redis.disconnect();
    await dbService.close();
    logger.info('Graceful shutdown completed');
    process.exit(0);
  } catch (error) {
    logger.error('Error during shutdown:', error);
    process.exit(1);
  }
});

// Start the bot
startup();

